/**
 * Генерирует массив товаров для указанного города.
 * @param city - Город ('gomel', 'rechitsa' или 'sublimation')
 * @param images - Массив URL изображений
 * @param startId - Начальный ID для нумерации товаров
 * @returns Массив объектов Product
 */
const buildProducts = (city: City, images: string[], startId: number): Product[] => {
  return images.slice(4).map((image, index) => {
    const preset = productPresets[city][index % productPresets[city].length];
    const title = preset[0];

    return {
      id: startId + index,
      image,
      title,
      category: preset[1],
      price: preset[2],
      material: preset[3],
      description: productDescriptions[title] || 
        'Авторское изделие для интерьера, вдохновлённое эстетикой игр, натуральными фактурами и тёплым ремесленным подходом.',
      city,
    };
  });
};

/**
 * Генерирует массив фотографий магазина для указанного города.
 * @param city - Город ('gomel', 'rechitsa' или 'sublimation')
 * @param images - Массив URL изображений
 * @param startId - Начальный ID для нумерации фотографий
 * @returns Массив объектов StorePhoto
 */
const buildStorePhotos = (
  city: City,
  images: string[],
  startId: number,
): StorePhoto[] => {
  return images.slice(0, 4).map((image, index) => {
    const preset = locationPresets[city][index % locationPresets[city].length];

    return {
      id: startId + index,
      image,
      title: preset[0],
      caption: preset[1],
      city,
    };
  });
};

